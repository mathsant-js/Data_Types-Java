// Operações matemáticas
package defaultpackage;

public class Operacoes_metematicas {

	public static void main(String[] args) {
		// Declaração de variáveis
		double num1 = 9, num2 = 5, res;
		
		// Soma
		res = num1 + num2;
		System.out.println(num1 + " + " + num2 + " = " + res);
		
		// Subtração
		res = num1 - num2;
		System.out.println(num1 + " - " + num2 + " = " + res);
		
		// Multiplicação
		res = num1 * num2;
		System.out.println(num1 + " * " + num2 + " = " + res);
		
		// Divisão
		res = num1 / num2;
		System.out.println(num1 + " / " + num2 + " = " + res);
	}

}
